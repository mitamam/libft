#include "libft.h"

void	*ft_memset(void *buf, int ch, size_t n)
{
	unsigned char	*str;
	unsigned char	c;

	str = (unsigned char *)buf;
	c = (unsigned char)ch;
	while (n-- > 0)
		*str++ = c;
	return (buf);
}
