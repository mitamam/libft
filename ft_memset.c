#include "libft.h"

void	*ft_memset(void *buf, int c, size_t n)
{
	unsigned char	*str;
	unsigned char	ch;

	str = (unsigned char *)buf;
	ch = (unsigned char)c;
	while (n-- > 0)
		*str++ = ch;
	return (buf);
}
