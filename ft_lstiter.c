#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (lst == (void *)0)
		return ;
	if (lst->next != NULL)
	{
		f(lst->content);
		return (ft_lstiter(lst->next, f));
	}
	f(lst->content);
	return ;
}
